x = eval(input("Pick a number, any number:\t"))
y = eval(input("Pick another number, any number:\t"))
add = x + y
print("Addition")
print(x, "+", y, "=", add)
subtract = x - y
print("Subtraction")
print(x, "-", y, "=", subtract)
multiply = x * y
print("Multiplication")
print(x, "x", y, "=", multiply)
divide = x + y
print("Division")
print(x, "÷", y, "=", divide)
root1 = x**0.5
root2 = y**0.5
print("Square roots")
print("√" + str(x), "=", root1)
print("√" + str(y), "=", root2)
